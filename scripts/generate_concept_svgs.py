PALETTE = ["#2563eb", "#7c3aed", "#0891b2", "#059669", "#d97706", "#dc2626"]


def diagram(filename, title, subtitle, nodes, links=(), footer="핵심 흐름과 구별 기준을 함께 확인하세요."):
    width, height = 960, 500
    node_markup = []
    for index, (x, y, w, h, heading, detail) in enumerate(nodes):
        color = PALETTE[index % len(PALETTE)]
        detail_lines = str(detail).splitlines()
        detail_markup = "".join(
            f'<tspan x="{x + 24}" y="{y + 65 + line_index * 24}">{escape(line)}</tspan>'
            for line_index, line in enumerate(detail_lines)
        )
        node_markup.append(
            f'<g><rect x="{x}" y="{y}" width="{w}" height="{h}" rx="18" fill="#111c35" stroke="{color}" stroke-width="2"/>'
            f'<rect x="{x}" y="{y}" width="8" height="{h}" rx="4" fill="{color}"/>'
            f'<text x="{x + 24}" y="{y + 35}" class="heading">{escape(heading)}</text>'
            f'<text class="detail">{detail_markup}</text></g>'
        )
    link_markup = []
    for x1, y1, x2, y2, label in links:
        link_markup.append(
            f'<path d="M{x1} {y1} L{x2} {y2}" class="arrow" marker-end="url(#arrow)"/>'
            + (f'<text x="{(x1 + x2) / 2}" y="{(y1 + y2) / 2 - 8}" class="link">{escape(label)}</text>' if label else "")
        )
    svg = f'''<svg xmlns="http://www.w3.org/2000/svg" width="960" height="500" viewBox="0 0 960 500" role="img" aria-labelledby="title desc">
  <title id="title">{escape(title)}</title><desc id="desc">{escape(subtitle)}</desc>
  <defs><linearGradient id="bg" x1="0" y1="0" x2="1" y2="1"><stop stop-color="#071126"/><stop offset="1" stop-color="#0f172a"/></linearGradient><marker id="arrow" markerWidth="10" markerHeight="10" refX="9" refY="3" orient="auto" markerUnits="strokeWidth"><path d="M0,0 L0,6 L9,3 z" fill="#7dd3fc"/></marker></defs>
  <style>.title{{fill:#f8fafc;font:700 30px system-ui,sans-serif}}.subtitle{{fill:#94a3b8;font:16px system-ui,sans-serif}}.heading{{fill:#e2e8f0;font:700 18px system-ui,sans-serif}}.detail{{fill:#a5b4c7;font:14px system-ui,sans-serif}}.arrow{{stroke:#7dd3fc;stroke-width:2.5;fill:none}}.link{{fill:#67e8f9;font:13px system-ui,sans-serif;text-anchor:middle}}.footer{{fill:#93c5fd;font:14px system-ui,sans-serif}}</style>
  <rect width="960" height="500" rx="28" fill="url(#bg)"/><text x="42" y="55" class="title">{escape(title)}</text><text x="42" y="84" class="subtitle">{escape(subtitle)}</text>
  {''.join(link_markup)}{''.join(node_markup)}
  <text x="42" y="470" class="footer">{escape(footer)}</text>
</svg>'''
    (OUT / filename).write_text(svg, encoding="utf-8")


